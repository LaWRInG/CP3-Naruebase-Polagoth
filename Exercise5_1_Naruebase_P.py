x = int(input("Please input the first integer number : "))
y = int(input("Please input the second integer number : "))

result_1 = x + y
result_2 = x - y
result_3 = x * y
result_4 = x / y

print(x, '+', y, "=", result_1)
print(x, "-", y, '=', result_2)
print(x, "*", y, "=", result_3)
print(x, "/", y, "=", result_4)
